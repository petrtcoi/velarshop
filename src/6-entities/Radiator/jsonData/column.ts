import { arrayRange } from '@shared/utils/arrayRange';
import type { RadiatorJson } from '../types/RadiatorJson.type';

const MIN_SECTIONS = 4;

export function getColumnRadiators(): RadiatorJson[] {
  return columnModels
    .map(model => {
      const { sections, length, price, dt50, dt60, dt70, weight, volume } =
        model;

      const sectionsArray = arrayRange(
        MIN_SECTIONS,
        parseInt(sections || '4'),
        1
      );
      return sectionsArray.map(sections => {
        if (!price || !dt50 || !dt60 || !dt70 || !weight || !volume)
          return { ...model, sections: '1' };

        return {
          ...model,
          slug: `${model.slug}-${sections}`,
          price: (parseInt(price) * sections).toString(),
          dt50: (parseInt(dt50) * sections).toFixed(0),
          dt60: (parseInt(dt60) * sections).toFixed(0),
          dt70: (parseInt(dt70) * sections).toFixed(0),
          weight: (parseFloat(weight) * sections).toFixed(2),
          volume: (parseFloat(volume) * sections).toFixed(2),
          sections: sections.toString(),
          length: (parseInt(length) * sections + 18).toFixed(0),
        };
      });
    })
    .flat();
}

function roundFor2DigitsAfterSeparator(value: string) {
  return parseFloat(value).toFixed(2);
}

